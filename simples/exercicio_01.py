# Escreva um programa para ler o raio de um círculo, calcular e escrever a sua área (Pi * (raio * raio))

#Entrada
raio = int(input('Raio:'))       #Variavel
PI = 3.14      #Constante
#Processamento
area = PI * (raio * raio)
#Saida
print('A area do circulo é:',area)
