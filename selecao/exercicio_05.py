'''Escreva um programa para ler um valor e escrever se é positivo ou negativo. Considere o valor zero como positivo.'''

#Entrada
valor = int(input("Insira um numero: "))

#Processamento
reposta = ''
if valor >= 0:
    valor = "Positivo"
else:
    valor < 0
    valor = "Negativo"    


#Saída
print(f"Seu número é {reposta}")
